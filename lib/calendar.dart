                                onTap: () => print('${value[index].id}'),
                                title: Text('${value[index].title}'),
                              ),
                            );
                          },
                        );
                      },
                    ),
                  ),
                ],
              );
            } else {
              return Center(child: CircularProgressIndicator());
            }
          }),
